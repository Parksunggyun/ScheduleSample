package altong.mon.schedulesample;

public class TypeColor {
    private String red;
    private String green;
    private String blue;

    public TypeColor(String red, String green, String blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    public String getRed() {
        return red;
    }

    public String getGreen() {
        return green;
    }

    public String getBlue() {
        return blue;
    }
}
